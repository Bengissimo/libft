#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	i;

	i = 0;
	while (src[i] != '\0' && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	while (len-- > ft_strlen(src))
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
